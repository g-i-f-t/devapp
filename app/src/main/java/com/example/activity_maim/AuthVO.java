package com.example.activity_maim;

public class AuthVO {
    private Long code;
    private String messages;
    private String access_token;
    private String user_seq_no;

    public Long getCode() {
        return code;
    }

    public String getMessages() {
        return messages;
    }

    public String getAccess_token() {
        return access_token;
    }

    public String getUser_seq_no() {
        return user_seq_no;
    }
}
